package Hoorcollege.Gui;

public class AgeCategorie {
    int id;
    String text;

    public AgeCategorie(int id, String text) {
        this.id = id;
        this.text = text;
    }

    public int getId() {
        return id;
    }

    public String getText() {
        return text;
    }

    public String toString() {
        return text;
    }
}
